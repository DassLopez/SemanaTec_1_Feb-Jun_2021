def move():
    if((c[0] == 1) and (c[1] == 1) and (c[2] == 1)):
        z = m_2
    else:
        z = m
    "Move pacman and all ghosts."
    writer.undo()
    writer.write(state['score'])

    clear()

    if valid(pacman + aim):
        pacman.move(aim)

    index = offset(pacman)

    if tiles[index] == 1:
        tiles[index] = 2
        x = (index % 20) * 20 - 200
        y = 180 - (index // 20) * 20
        # print("X: "+str(x)+" Y: "+str(y))
